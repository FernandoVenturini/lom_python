# INTRODUCAO AOS OPERADORES ARITMETICOS
# Operadores Aritmeticos: +, -, *, /, //, **, %

# Soma
adicao = 10 + 10
print("Adição: ", adicao)

# Subtração
subtracao = 10 - 5
print("Subtração: ", subtracao)

# Multiplicação
multiplicacao = 10 * 10
print("Multiplicação: ", multiplicacao)

# Divisão
divisao = 10 / 3
print("Divisão: ", divisao)

# Divisão Inteira
divisao_inteira = 10 // 3
print("Divisão Inteira: ", divisao_inteira)

# Potência
potencia = 10 ** 5
print("Potência: ", potencia)

# Módulo
modulo = 55 % 2 # O operador de módulo retorna o resto da divisão entre dois números. Neste caso, 10 dividido por 5 é igual a 2, e o resto é 0, portanto o resultado é 0.
print("Módulo: ", modulo)

# Tipo bool (bolean): true ou false
# Operadores de comparação: ==, !=, >, <, >=, <=
# Operadores lógicos: and, or, not

# Exemplo de comparação de idade:
idade = 18
print(idade >= 18)  # True, pois 18 é maior ou igual a 18

# Exemplo de comparação de strings:
nome1 = "Alice"
nome2 = "Bob"
print(nome1 == nome2)  # False, pois "Alice" é diferente de "Bob"
print(type(nome1 == nome2))  # <class 'bool'>, o resultado é do tipo booleano
