# Tipo bool (bolean): true ou false
# Operadores de comparação: ==, !=, >, <, >=, <=
# Operadores lógicos: and, or, not

# Exemplo de comparação de idade:
idade = 18
print(idade >= 18)  # True, pois 18 é maior ou igual a 18

# Exemplo de comparação de strings:
nome1 = "Alice"
nome2 = "Bob"
print(nome1 == nome2)  # False, pois "Alice" é diferente de "Bob"
print(type(nome1 == nome2))  # <class 'bool'>, o resultado é do tipo booleano