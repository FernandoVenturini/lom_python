# Tipo int e float
# int: números inteiros
# float: números decimais

# Exemplo de int
idade = 25
print("Idade:", idade)

# Exemplo de float
altura = 1.75
print("Altura:", altura)

print(type('Fernando'))  # Saída: <class 'str'>
print(type(idade))  # Saída: <class 'int'>
print(type(altura))  # Saída: <class 'float'>